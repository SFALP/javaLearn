//    -=       x -= 3     x = x - 3
//    *=       x *= 3     x = x * 3
//    /=       x /= 3     x = x / 3
//    %=       x %= 3     x = x % 3
    int yas= 33;
        System.out.println(yas+5);//38 -> Atama (yani = ifadesi) olmadığı ,için yaş=33 kaldı.

